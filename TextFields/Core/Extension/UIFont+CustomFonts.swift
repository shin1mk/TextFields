import UIKit

extension UIFont {
    static func customFontRegular(ofSize size: CGFloat) -> UIFont? {
        return UIFont(name: "Rubik-Regular", size: size)
    }
    static func customFontMedium(ofSize size: CGFloat) -> UIFont? {
        return UIFont(name: "Rubik-Medium", size: size)
    }
}
